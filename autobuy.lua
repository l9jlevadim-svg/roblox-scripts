    local items = {}
    for _, item in ipairs(clothes) do
        if not item.taken and not item.unavailable and shouldBuyItem(item) then
            table.insert(items, item)
        end
    end
    local shopOrder = {}
    for i, shop in ipairs(route) do shopOrder[shop.name] = i end
    table.sort(items, function(a, b)
        local orderA = shopOrder[a.shop] or 999
        local orderB = shopOrder[b.shop] or 999
        if orderA ~= orderB then return orderA < orderB end
        local distA = a.position and (a.position - rootPart.Position).Magnitude or 9999
        local distB = b.position and (b.position - rootPart.Position).Magnitude or 9999
        return distA < distB
    end)
    return items
end

